// Clean and prepare dist folder
function prepareDist() {
  print('Preparing distribution folder...');
  
  if (fs.existsSync(DIST)) {
    fs.rmSync(DIST, { recursive: true, force: true });
  }
  fs.mkdirSync(DIST, { recursive: true });
  
  // Copy all necessary files
  const filesToCopy = [
    'bin/stingray.js', 'bin/stingray.bat', 'bin/stngr.bat', 'bin/sr.bat',
    'bin/stingray-new-project.bat', 'bin/stingray-dev.bat',
    'bin/stingray-build.bat', 'bin/stingray-run.bat',
    'bin/stingray-serve.bat', 'bin/stingray-open.bat',
    'package.json', 'stingray.json',
    'Assets/logo.png'
  ];
  
  const dirsToCopy = ['src', 'dist', 'extension', 'examples', 'tests'];
  
  for (const f of filesToCopy) {
    const src = path.join(ROOT, f);
    const dst = path.join(DIST, f);
    if (fs.existsSync(src)) {
      const dstDir = path.dirname(dst);
      if (!fs.existsSync(dstDir)) fs.mkdirSync(dstDir, { recursive: true });
      fs.copyFileSync(src, dst);
    }
  }
  
  for (const d of dirsToCopy) {
    const src = path.join(ROOT, d);
    const dst = path.join(DIST, d);
    if (fs.existsSync(src)) {
      copyDirRecursive(src, dst);
    }
  }
  
  // Create a standalone launcher
  createStandaloneLauncher();
  
  printSuccess('Distribution folder prepared');
}

function copyDirRecursive(src, dst) {
  if (!fs.existsSync(dst)) fs.mkdirSync(dst, { recursive: true });
  const items = fs.readdirSync(src, { withFileTypes: true });
  for (const item of items) {
    const srcPath = path.join(src, item.name);
    const dstPath = path.join(dst, item.name);
    if (item.isDirectory()) {
      copyDirRecursive(srcPath, dstPath);
    } else {
      fs.copyFileSync(srcPath, dstPath);
    }
  }
}

function createStandaloneLauncher() {
  print('Creating standalone launcher...');
  
  // Create a self-contained .bat that bundles everything
  const launcherPath = path.join(DIST, 'stingray.exe.bat');
  fs.writeFileSync(launcherPath, `@echo off
set STINGRAY_ROOT=%~dp0
node "%STINGRAY_ROOT%\\bin\\stingray.js" %*
`);
  
  // Create a .lnk-style shortcut info
  const infoPath = path.join(DIST, 'README.txt');
  fs.writeFileSync(infoPath, 
    'Stingray Language v1.0.0\n' +
    'HTML + CSS + JS + React in one file (.stngr)\n\n' +
    'Quick Start:\n' +
    '  stingray init my-app     - Create new project\n' +
    '  stingray dev             - Start dev server\n' +
    '  stingray build           - Build for production\n' +
    '  stingray run app.stngr   - Compile and run\n\n' +
    'Shortcuts: sr, stngr, sdev, sb\n\n' +
    'Extension: .stngr\n' +
    'Logo: Assets/logo.png\n'
  );
  
  printSuccess('Standalone launcher created');
}

